# 函数 ， 所有高级语言都支持函数，python不但能非常灵活地定义函数，而且本身内置了很多有用的函数，可以直接调用。
# 抽象 ， 是数学中非常常见的概念，比如1+2+3...+100，写起来十分不方便，于是数学家发明了求和符号∑，可以把1+2+3...+100
# 记作 100
#      ∑n
#      n=1
# 这种抽象记法非常强大，因为我们看到∑就可以理解成求和，而不是还原成低级的加法运算。
# 而且，这种抽象记法是可扩展的
# 比如 100
#      ∑(n2+1)
#       n=1
# 还原成加法运算就变成了：(1 x 1 + 1) + (2 x 2 + 1) + (3 x 3 + 1) + ... + (100 x 100 + 1)
# 可见，借助抽象，我们才能不关心底层的具体计算过程，而直接在更高的层次上思考问题。
# 函数 ， 就是最基本的一种代码抽象的方式。